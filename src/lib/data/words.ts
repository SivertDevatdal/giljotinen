function getDayOfYear(d: Date): number {
	const start = new Date(d.getFullYear(), 0, 0);
	const diff = d.getTime() - start.getTime();
	return Math.floor(diff / 86400000);
}

export interface DailyWord extends WordEntry {
	dayNum: number;
	year: number;
	dateStr: string;
}

export function getWordForDay(): DailyWord {
	const now = new Date();
	const day = getDayOfYear(now);
	const year = now.getFullYear();
	const idx = ((year * 366) + day) % WORDS.length;
	const dateStr = now.toLocaleDateString('nb-NO', { day: 'numeric', month: 'long', year: 'numeric' });
	return { ...WORDS[idx], dayNum: day, year, dateStr };
}
